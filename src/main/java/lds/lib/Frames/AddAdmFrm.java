    private void setInfo() {
        String name = this.emp.getFirstname() +" "+ this.emp.getLastname();
        
        String position;
        switch(this.emp.getPosition()) {
            case 1  : position = "Courier"; break;
            case 2  : position = "Staff"; break;
            case 3  : position = "Supervisor"; break;
            default : position = "Not Positioned Yet";
        }
        
        this.txtName.setText(name);
        this.txtPos.setText(position);
    }
    
    private boolean checkUsrnm() {
        return this.admcon.getAdmByUsername(this.txtUsername.getText()) == null;
    }
    
    private void checkMatch() {
        if (Arrays.equals(this.passNew.getPassword(), this.passCon.getPassword())) {
            this.labelNotif.setForeground(Color.GREEN);
            this.labelNotif.setText("New Password match !");
        } else {
           this.labelNotif.setForeground(Color.RED);
           this.labelNotif.setText("New Password did not match");
        }
    }
    
    private boolean isEmpty() {
        return  this.txtUsername.getText() == null || 
                this.txtUsername.getText().equals("") ||
                this.passNew.getPassword() == null || 
                this.passCon.getPassword() == null || 
                this.radGroup.getSelection() == null;
    }
    
    private int getPermit() {
        if(this.radGroup.getSelection() == this.radSup) {
            return 1;
        }
        return 0;
    }
   
