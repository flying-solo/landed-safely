    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(!this.isEmpty()) {
            if(this.checkUsrnm()) {
                Admin admin = new Admin();
                String salt = Encryption.generateSalt();
                String pass = Encryption.encryptPass(new String(this.passCon.getPassword()), salt);
                
                admin.setId_employee(this.emp.getId());
                admin.setUsername(this.txtUsername.getText());
                admin.setPassword(pass);
                admin.setSalt(salt);
                admin.setPermit(this.getPermit());
                
                if(this.admcon.insertAdmin(admin)) {
                    StringBuilder sb = new StringBuilder();
                    sb.append("Account Created");
                    String ttl = "LDS : Information";
                    int type = JOptionPane.INFORMATION_MESSAGE;
                    this.main.userDialog(sb, ttl, type);
                    this.dispose();
                } else {
                    this.labelNotif.setForeground(Color.RED);
                    this.labelNotif.setText(
                        "Failed to create Admin account."
                    );
                }
            } else {
                this.labelNotif.setForeground(Color.RED);
                this.labelNotif.setText(
                    "Username : "+ this.txtUsername.getText() +", Has taken."
                );
            }
        } else {
           this.labelNotif.setForeground(Color.RED);
           this.labelNotif.setText("Fields with * markers cannot be empty.");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

